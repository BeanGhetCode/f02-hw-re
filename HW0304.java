package OperatorAndConditions;

public class HW04 {
    public static void main(String[] args) {
        int a = 6;
        int b = 9;
        if (a > b ){
            System.out.println(a + " > " + b);
        } else if (a < b) {
            System.out.println(a + " < " + b);
        }else{
            System.out.println(a + " = " + b);
        }
    }
}
